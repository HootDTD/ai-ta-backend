DO $postconditions$
DECLARE
    app_table_count integer;
    internal_table_count integer;
    unprotected_app_tables text;
BEGIN
    SELECT count(*) INTO app_table_count
    FROM pg_tables WHERE schemaname = 'app';
    SELECT count(*) INTO internal_table_count
    FROM pg_tables WHERE schemaname = 'internal';

    IF app_table_count <> 21 OR internal_table_count <> 12 THEN
        RAISE EXCEPTION 'DB-04 postcondition failed: expected 21 app + 12 internal tables, got % + %',
            app_table_count, internal_table_count;
    END IF;

    SELECT string_agg(format('%I.%I', n.nspname, c.relname), ', ' ORDER BY c.relname)
    INTO unprotected_app_tables
    FROM pg_class AS c
    JOIN pg_namespace AS n ON n.oid = c.relnamespace
    WHERE n.nspname = 'app'
      AND c.relkind = 'r'
      AND (NOT c.relrowsecurity OR NOT c.relforcerowsecurity);

    IF unprotected_app_tables IS NOT NULL THEN
        RAISE EXCEPTION 'DB-04 postcondition failed: app tables without forced RLS: %',
            unprotected_app_tables;
    END IF;
END
$postconditions$;

