/*
 * DB-04: build the v1 target beside the frozen legacy public schema.
 *
 * This migration is intentionally DDL-only. It does not copy data and it does
 * not alter, rename, or drop any legacy public table.
 */

SET lock_timeout = '5s';
SET statement_timeout = '5min';

BEGIN;

DO $preconditions$
BEGIN
    IF EXISTS (SELECT 1 FROM pg_namespace WHERE nspname = 'app') THEN
        RAISE EXCEPTION 'DB-04 precondition failed: schema app already exists';
    END IF;
    IF EXISTS (SELECT 1 FROM pg_namespace WHERE nspname = 'internal') THEN
        RAISE EXCEPTION 'DB-04 precondition failed: schema internal already exists';
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_extension WHERE extname = 'vector') THEN
        RAISE EXCEPTION 'DB-04 precondition failed: vector extension is not installed';
    END IF;
    IF NOT EXISTS (
        SELECT 1
        FROM information_schema.tables
        WHERE table_schema = 'public' AND table_name = 'aita_search_spaces'
    ) THEN
        RAISE EXCEPTION 'DB-04 precondition failed: legacy public snapshot is missing';
    END IF;
END
$preconditions$;

CREATE SCHEMA app;
CREATE SCHEMA internal;
CREATE SCHEMA IF NOT EXISTS extensions;

-- pgvector objects are OID-linked, so legacy columns remain valid after the
-- extension namespace move. DB-03/DB-04 local rehearsal is the safety gate.
ALTER EXTENSION vector SET SCHEMA extensions;

DO $runtime_role$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'app_runtime') THEN
        CREATE ROLE app_runtime NOLOGIN NOBYPASSRLS;
    ELSIF EXISTS (
        SELECT 1 FROM pg_roles WHERE rolname = 'app_runtime' AND rolbypassrls
    ) THEN
        RAISE EXCEPTION 'DB-04 precondition failed: app_runtime must not BYPASSRLS';
    END IF;
END
$runtime_role$;

