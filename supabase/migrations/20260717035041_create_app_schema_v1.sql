    p_chunk_ids bigint[],
    p_document_ids bigint[]
)
RETURNS SETOF internal.document_chunks
LANGUAGE sql
STABLE
SET search_path = ''
AS $function$
    SELECT chunks.*
    FROM internal.document_chunks AS chunks
    WHERE chunks.id = ANY (p_chunk_ids)
      AND chunks.document_id = ANY (p_document_ids)
$function$;

CREATE FUNCTION internal.fts_count(
    query_text text,
    p_document_ids bigint[]
)
RETURNS integer
LANGUAGE sql
STABLE
SET search_path = ''
AS $function$
    SELECT count(*)::integer
    FROM internal.document_chunks AS chunks
    WHERE chunks.document_id = ANY (p_document_ids)
      AND pg_catalog.to_tsvector('english', chunks.content)
          @@ pg_catalog.websearch_to_tsquery('english', query_text)
$function$;

CREATE FUNCTION internal.hybrid_search(
    query_text text,
    query_embedding extensions.vector(3072),
    p_document_ids bigint[],
    match_count integer,
    rrf_k integer DEFAULT 60
)
RETURNS TABLE (
    chunk_id bigint,
    document_id bigint,
    semantic_score double precision,
    lexical_score double precision,
    semantic_rank integer,
    lexical_rank integer
)
LANGUAGE sql
STABLE
SET search_path = ''
AS $function$
    WITH sem AS (
        SELECT
            chunks.id,
            chunks.document_id,
            1.0 - (
                chunks.embedding::extensions.halfvec(3072)
                OPERATOR(extensions.<=>)
                query_embedding::extensions.halfvec(3072)
            ) AS score,
            row_number() OVER (
                ORDER BY
                    chunks.embedding::extensions.halfvec(3072)
                    OPERATOR(extensions.<=>)
                    query_embedding::extensions.halfvec(3072)
            )::integer AS rnk
        FROM internal.document_chunks AS chunks
        ORDER BY
            chunks.embedding::extensions.halfvec(3072)
            OPERATOR(extensions.<=>)
            query_embedding::extensions.halfvec(3072)
        LIMIT match_count * 10
    ),
    sem_filtered AS (
        SELECT * FROM sem
        WHERE document_id = ANY (p_document_ids)
        LIMIT match_count * 2
    ),
    lex AS (
        SELECT
            chunks.id,
            chunks.document_id,
            pg_catalog.ts_rank_cd(
                pg_catalog.to_tsvector('english', chunks.content),
                pg_catalog.websearch_to_tsquery('english', query_text)
            ) AS score,
            row_number() OVER (
                ORDER BY pg_catalog.ts_rank_cd(
                    pg_catalog.to_tsvector('english', chunks.content),
                    pg_catalog.websearch_to_tsquery('english', query_text)
                ) DESC
            )::integer AS rnk
        FROM internal.document_chunks AS chunks
        WHERE chunks.document_id = ANY (p_document_ids)
          AND pg_catalog.to_tsvector('english', chunks.content)
              @@ pg_catalog.websearch_to_tsquery('english', query_text)
        LIMIT match_count * 2
    )
    SELECT
        coalesce(sem_filtered.id, lex.id),
        coalesce(sem_filtered.document_id, lex.document_id),
        coalesce(sem_filtered.score, 0.0)::double precision,
        coalesce(lex.score, 0.0)::double precision,
        coalesce(sem_filtered.rnk, match_count * 2 + 1)::integer,
        coalesce(lex.rnk, match_count * 2 + 1)::integer
    FROM sem_filtered
    FULL OUTER JOIN lex
      ON sem_filtered.id = lex.id
     AND sem_filtered.document_id = lex.document_id
    ORDER BY (
        1.0 / (rrf_k + coalesce(sem_filtered.rnk, match_count * 2 + 1))
        + 1.0 / (rrf_k + coalesce(lex.rnk, match_count * 2 + 1))
    ) DESC
    LIMIT match_count
$function$;

