async def test_seed_links_every_reference_node_to_an_entity(seeded_db):
    """Every reference-solution step gets linked to a real ``app.learner_entities``
    row. DB-13: ``keys`` never contains a ``kind='misconception'`` canonical_key
    (none are ever minted, see ``test_seed_does_not_create_misconception_entities``)
    — reference-solution steps never carry a misconception ``entry_type`` either
    (misconceptions come from the separate, no-longer-read ``misconceptions.json``,
    never from a problem's ``reference_solution``), so misconception reference
    nodes are excluded from this every-node-links assertion by construction on
    both sides of the comparison."""
    sa_dsn, plain = await seeded_db(_seed_one_course)
    await seed(sa_dsn)
    concept_id = await _fetchval(
        plain, "SELECT id FROM app.concepts WHERE slug='bernoulli_principle'"
    )
    keys = {
        r["canonical_key"]
        for r in await _fetch(
            plain,
            "SELECT canonical_key FROM app.learner_entities "
            "WHERE concept_id=$1 AND kind != 'misconception'",
            concept_id,
        )
    }
    rows = await _fetch_target_problem_payloads(plain, concept_id)
    assert len(rows) == 5
    for row in rows:
        payload = json.loads(row["payload"])
        for step in payload["reference_solution"]:
            if step.get("entry_type") == "misconception":
                continue  # excluded: never minted, never entity-linked (DB-13)
            assert step.get("entity_key"), f"unlinked step {step.get('id')}"
            assert step["entity_key"] in keys


