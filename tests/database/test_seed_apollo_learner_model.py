async def test_seed_does_not_create_misconception_entities(seeded_db):
    """DB-13/A6 ruling: the seeder never reads ``misconceptions.json`` (see
    ``scripts/seed_apollo_learner_model.py``'s ``_collect_entity_specs``) and
    ``app.learner_entities``' target DDL kind CHECK
    (``learner_entities__kind__check``) has no ``'misconception'`` value.

    This replaces the pre-DB-13 assertion that ``misc.*`` entities + an
    opposes-link were minted (``misc.density_ignored`` opposing
    ``cond.incompressibility``) — that behavior was deliberately removed;
    misconceptions are excluded from the Postgres Layer-1 skill inventory
    entirely. The surviving kinds (concept/variable/equation/condition/
    procedure/definition) are still minted normally."""
    sa_dsn, plain = await seeded_db(_seed_one_course)
    await seed(sa_dsn)
    concept_id = await _fetchval(
        plain, "SELECT id FROM app.concepts WHERE slug='bernoulli_principle'"
    )
    misconception_count = await _fetchval(
        plain,
        "SELECT count(*) FROM app.learner_entities WHERE concept_id=$1 AND kind='misconception'",
        concept_id,
    )
    assert misconception_count == 0

    # No row anywhere carries the legacy misconception canonical_key either.
    density_ignored = await _fetchval(
        plain,
        "SELECT count(*) FROM app.learner_entities "
        "WHERE concept_id=$1 AND canonical_key='misc.density_ignored'",
        concept_id,
    )
    assert density_ignored == 0

    kinds = {
        r["kind"]
        for r in await _fetch(
            plain,
            "SELECT DISTINCT kind FROM app.learner_entities WHERE concept_id=$1",
            concept_id,
        )
    }
    assert "misconception" not in kinds
    # The surviving kinds are still minted from the concept dag, canonical
    # symbols, reference solutions, and the bernoulli authored definition.
    assert kinds == {"concept", "variable", "equation", "condition", "procedure", "definition"}


